import PDFDocument from "pdfkit";

export async function generateMonthlyReportPDF(
  stats,
  monthName,
  userName,
  insights
) {
  return new Promise((resolve, reject) => {
    const doc = new PDFDocument({
      margin: 50,
      size: "A4",
    });

    const buffers = [];

    doc.on("data", (chunk) =>
      buffers.push(chunk)
    );

    doc.on("end", () =>
      resolve(Buffer.concat(buffers))
    );

    doc.on("error", reject);

    // Title
    doc
      .fontSize(24)
      .fillColor("#1e293b")
      .text(
        "SmartSpend Financial Report",
        {
          align: "center",
        }
      );

    doc.moveDown();

    doc
      .fontSize(14)
      .fillColor("#475569")
      .text(`Month: ${monthName}`);

    doc.text(`User: ${userName}`);

    doc.moveDown();

    // Summary
    doc
      .fontSize(18)
      .fillColor("#111827")
      .text("Financial Summary");

    doc.moveDown(0.5);

    doc
      .fontSize(12)
      .fillColor("#16a34a")
      .text(
        `Total Income: $${stats.totalIncome.toFixed(
          2
        )}`
      );

    doc
      .fillColor("#dc2626")
      .text(
        `Total Expenses: $${stats.totalExpenses.toFixed(
          2
        )}`
      );

    doc
      .fillColor("#2563eb")
      .text(
        `Net Savings: $${(
          stats.totalIncome -
          stats.totalExpenses
        ).toFixed(2)}`
      );

    doc.moveDown();

    // Categories
    doc
      .fontSize(18)
      .fillColor("#111827")
      .text("Expenses by Category");

    doc.moveDown(0.5);

    Object.entries(
      stats.byCategory || {}
    ).forEach(([category, amount]) => {
      doc
        .fontSize(12)
        .fillColor("#374151")
        .text(
          `${category}: $${Number(
            amount
          ).toFixed(2)}`
        );
    });

    doc.moveDown();

      // AI Insights
      if (insights?.length > 0) {
        doc.moveDown();

        doc
          .fontSize(18)
          .fillColor("#111827")
          .text("AI Financial Insights");

        doc.moveDown(0.5);

        insights.forEach((insight) => {
          doc
            .fontSize(12)
            .fillColor("#374151")
            .text(`• ${insight}`);
        });
      }

      doc.moveDown();

      doc
        .fontSize(10)
        .fillColor("#6b7280")
        .text(
          "Generated by SmartSpend AI Expense Tracker",
          {
            align: "center",
          }
        );

    doc.end();
  });
}